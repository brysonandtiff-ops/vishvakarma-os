#!/usr/bin/env node
/**
 * Renders the iOS `apple-touch-startup-image` set into public/splash/ from the
 * derived self-contained swan icon (public/icons/icon.svg) — the same artwork
 * used by the in-app startup splash (index.html #boot-splash).
 *
 * Each image is a static launch screen: the swan logo + VISHVAKARMA.OS wordmark
 * centred on the #17120c brand stage, sized to a device's physical pixel
 * resolution. The matching <link rel="apple-touch-startup-image"> tags live in
 * index.html and MUST stay in sync with DEVICES below (the script prints the
 * full tag block at the end so they can be regenerated by hand if devices change).
 *
 * Run: pnpm assets:ios-splash
 */
import { chromium } from '@playwright/test';
import { existsSync, mkdirSync, readFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const iconSvgPath = join(root, 'public', 'icons', 'icon.svg');
const outDir = join(root, 'public', 'splash');

const STAGE = '#17120c';
const WORDMARK = '#f4ead6';

// CSS-point device dimensions (portrait) + device pixel ratio. iPhones ship
// portrait-only (PWAs launch portrait); iPads ship portrait + landscape because
// this is an iPad-first app commonly used in landscape.
const DEVICES = [
  { label: 'iPhone SE / 8', w: 375, h: 667, ratio: 2, orientations: ['portrait'] },
  { label: 'iPhone XR / 11', w: 414, h: 896, ratio: 2, orientations: ['portrait'] },
  { label: 'iPhone X / 11 Pro / 13 mini', w: 375, h: 812, ratio: 3, orientations: ['portrait'] },
  { label: 'iPhone 12-14', w: 390, h: 844, ratio: 3, orientations: ['portrait'] },
  { label: 'iPhone 15 / 16', w: 393, h: 852, ratio: 3, orientations: ['portrait'] },
  { label: 'iPhone Pro Max / Plus', w: 430, h: 932, ratio: 3, orientations: ['portrait'] },
  { label: 'iPad mini / classic', w: 768, h: 1024, ratio: 2, orientations: ['portrait', 'landscape'] },
  { label: 'iPad Air 10.9', w: 820, h: 1180, ratio: 2, orientations: ['portrait', 'landscape'] },
  { label: 'iPad Pro 11 / Air', w: 834, h: 1194, ratio: 2, orientations: ['portrait', 'landscape'] },
  { label: 'iPad Pro 12.9', w: 1024, h: 1366, ratio: 2, orientations: ['portrait', 'landscape'] },
];

/** Expand DEVICES into concrete render targets (physical pixels + media query). */
function buildTargets() {
  const targets = [];
  for (const device of DEVICES) {
    for (const orientation of device.orientations) {
      const portrait = orientation === 'portrait';
      const pxW = (portrait ? device.w : device.h) * device.ratio;
      const pxH = (portrait ? device.h : device.w) * device.ratio;
      targets.push({
        file: `apple-splash-${pxW}-${pxH}.png`,
        pxW,
        pxH,
        media:
          `screen and (device-width: ${device.w}px) and (device-height: ${device.h}px) ` +
          `and (-webkit-device-pixel-ratio: ${device.ratio}) and (orientation: ${orientation})`,
      });
    }
  }
  return targets;
}

function buildSplashHtml(svgMarkup, pxW, pxH) {
  const minEdge = Math.min(pxW, pxH);
  const logo = Math.round(minEdge * 0.22);
  const fontSize = Math.round(minEdge * 0.03);
  const gap = Math.round(logo * 0.28);
  const radius = Math.round(logo * 0.24);
  return `<!DOCTYPE html>
<html>
  <head>
    <meta charset="utf-8" />
    <style>
      html, body { margin: 0; width: ${pxW}px; height: ${pxH}px; background: ${STAGE}; }
      /* Flat background (no gradient) so the large solid regions compress to near
         nothing as PNG — keeps each launch image small. */
      .stage {
        width: ${pxW}px;
        height: ${pxH}px;
        display: flex;
        flex-direction: column;
        align-items: center;
        justify-content: center;
        gap: ${gap}px;
        background: ${STAGE};
      }
      .icon { width: ${logo}px; height: ${logo}px; border-radius: ${radius}px; }
      .wordmark {
        margin: 0;
        font-family: system-ui, -apple-system, "Segoe UI", Roboto, sans-serif;
        font-weight: 700;
        font-size: ${fontSize}px;
        letter-spacing: 0.4em;
        text-indent: 0.4em;
        color: ${WORDMARK};
      }
    </style>
  </head>
  <body>
    <div class="stage">
      ${svgMarkup.replace('<svg', '<svg class="icon"')}
      <p class="wordmark">VISHVAKARMA.OS</p>
    </div>
  </body>
</html>`;
}

if (!existsSync(iconSvgPath)) {
  throw new Error(`Missing icon SVG: ${iconSvgPath}`);
}
mkdirSync(outDir, { recursive: true });

const svgMarkup = readFileSync(iconSvgPath, 'utf8');
const targets = buildTargets();
const browser = await chromium.launch();
try {
  for (const target of targets) {
    const page = await browser.newPage({ viewport: { width: target.pxW, height: target.pxH } });
    await page.setContent(buildSplashHtml(svgMarkup, target.pxW, target.pxH), { waitUntil: 'load' });
    await page.waitForTimeout(150);
    await page.screenshot({ path: join(outDir, target.file), type: 'png' });
    await page.close();
    console.log(`Wrote public/splash/${target.file}`);
  }
} finally {
  await browser.close();
}

// Emit the canonical <link> block so index.html can be kept in sync by hand.
console.log('\n--- apple-touch-startup-image link tags for index.html ---');
for (const target of targets) {
  console.log(
    `    <link rel="apple-touch-startup-image" media="${target.media}" href="/splash/${target.file}" />`,
  );
}
