#!/usr/bin/env node
/**
 * Regenerate docs/handoff/appendices/* from live repo sources.
 * Run: pnpm run handoff:generate
 */
import { execSync } from 'node:child_process';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '../..');
const OUT = path.join(ROOT, 'docs/handoff/appendices');

function read(file) {
  return fs.readFileSync(path.join(ROOT, file), 'utf8');
}

function write(name, body) {
  const header = `<!-- AUTO-GENERATED by scripts/handoff/generate-handoff-appendices.mjs â€” do not edit by hand -->\n\n`;
  fs.writeFileSync(path.join(OUT, name), header + body.trim() + '\n');
}

function gitSha() {
  try {
    return execSync('git rev-parse HEAD', { cwd: ROOT, encoding: 'utf8' }).trim();
  } catch {
    return 'unknown';
  }
}

function walkDir(dir, opts = {}) {
  const { exclude = new Set(), maxDepth = 6, depth = 0 } = opts;
  if (depth > maxDepth || !fs.existsSync(dir)) return [];
  const entries = [];
  for (const ent of fs.readdirSync(dir, { withFileTypes: true })) {
    const name = ent.name;
    if (exclude.has(name)) continue;
    if (name === 'node_modules' || name === 'dist' || name === 'coverage' || name === '.git') continue;
    if (name.startsWith('.') && name !== '.github' && name !== '.rules') continue;
    const full = path.join(dir, name);
    const rel = path.relative(ROOT, full).replace(/\\/g, '/');
    if (ent.isDirectory()) {
      entries.push(`${'  '.repeat(depth)}${name}/`);
      entries.push(...walkDir(full, { ...opts, depth: depth + 1 }));
    } else if (depth <= maxDepth) {
      entries.push(`${'  '.repeat(depth)}${name}`);
    }
  }
  return entries;
}

function parseRoutes() {
  const src = read('src/routes.tsx');
  const routes = [];
  const blockRe = /\{\s*name:\s*'([^']+)',\s*path:\s*'([^']+)',\s*element:\s*<(\w+)\s*\/>,[\s\S]*?access:\s*'(\w+)'/g;
  let m;
  while ((m = blockRe.exec(src)) !== null) {
    routes.push({ name: m[1], path: m[2], component: m[3], access: m[4] });
  }
  const pricingFlag = src.includes('PRICING_PAGE_ENABLED');
  return { routes, pricingFlag };
}

function parseApiRoutes() {
  const apiDir = path.join(ROOT, 'api');
  const handlers = [];
  function scan(dir, prefix = '') {
    for (const ent of fs.readdirSync(dir, { withFileTypes: true })) {
      const full = path.join(dir, ent.name);
      if (ent.isDirectory() && ent.name !== '_lib') {
        scan(full, `${prefix}/${ent.name}`);
      } else if (ent.name.endsWith('.ts') && !ent.name.startsWith('_')) {
        const rel = path.relative(ROOT, full).replace(/\\/g, '/');
        const route = `/api${prefix}/${ent.name.replace(/\.ts$/, '')}`;
        handlers.push({ route, file: rel });
      }
    }
  }
  scan(apiDir);
  return handlers.sort((a, b) => a.route.localeCompare(b.route));
}

function collectEnvVars() {
  const dirs = ['src', 'api', 'scripts', 'server'];
  const vite = new Set();
  const proc = new Set();
  const metaDev = new Set(['DEV', 'PROD', 'MODE']);
  const reVite = /import\.meta\.env\.([A-Z0-9_]+)/g;
  const reProc = /process\.env\.([A-Z0-9_]+)/g;

  for (const dir of dirs) {
    const base = path.join(ROOT, dir);
    if (!fs.existsSync(base)) continue;
    const files = execSync(`git ls-files "${dir}"`, { cwd: ROOT, encoding: 'utf8' })
      .split('\n')
      .filter((f) => f && /\.(ts|tsx|mjs|js|sh)$/.test(f));
    for (const file of files) {
      const content = fs.readFileSync(path.join(ROOT, file), 'utf8');
      let m;
      while ((m = reVite.exec(content)) !== null) vite.add(m[1]);
      while ((m = reProc.exec(content)) !== null) proc.add(m[1]);
    }
  }

  const exampleKeys = new Set();
  const example = read('.env.example');
  for (const line of example.split('\n')) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const key = trimmed.split('=')[0]?.trim();
    if (key) exampleKeys.add(key);
  }

  return { vite: [...vite].sort(), proc: [...proc].sort(), exampleKeys: [...exampleKeys].sort(), metaDev: [...metaDev] };
}

function parseMigrations() {
  const dir = path.join(ROOT, 'supabase/migrations');
  const files = fs.readdirSync(dir).filter((f) => f.endsWith('.sql')).sort();
  const tables = new Set();
  const policies = [];
  const triggers = [];
  const functions = [];

  for (const file of files) {
    const sql = fs.readFileSync(path.join(dir, file), 'utf8');
    for (const m of sql.matchAll(/create table(?: if not exists)?\s+public\.(\w+)/gi)) {
      tables.add(m[1]);
    }
    for (const m of sql.matchAll(/create policy (\w+) on (?:public\.)?(\w+)/gi)) {
      policies.push({ name: m[1], table: m[2], file });
    }
    for (const m of sql.matchAll(/create (?:or replace )?function public\.(\w+)/gi)) {
      functions.push({ name: m[1], file });
    }
    for (const m of sql.matchAll(/create trigger (\w+)/gi)) {
      triggers.push({ name: m[1], file });
    }
  }

  return { files, tables: [...tables].sort(), policies, functions, triggers };
}

function parseScripts() {
  const pkg = JSON.parse(read('package.json'));
  return Object.entries(pkg.scripts).sort(([a], [b]) => a.localeCompare(b));
}

function parseDeps() {
  const pkg = JSON.parse(read('package.json'));
  const prod = Object.entries(pkg.dependencies || {}).sort(([a], [b]) => a.localeCompare(b));
  const dev = Object.entries(pkg.devDependencies || {}).sort(([a], [b]) => a.localeCompare(b));
  return { prod, dev, version: pkg.version, name: pkg.name };
}

function globTests() {
  const out = { unit: [], e2e: [], anchors: [] };
  const unitFiles = execSync('git ls-files "src/**/*.test.*" "src/**/*.spec.*"', { cwd: ROOT, encoding: 'utf8' })
    .split('\n')
    .filter(Boolean);
  out.unit = unitFiles.sort();
  if (fs.existsSync(path.join(ROOT, 'e2e'))) {
    out.e2e = fs.readdirSync(path.join(ROOT, 'e2e')).filter((f) => f.endsWith('.spec.ts')).sort();
  }
  if (fs.existsSync(path.join(ROOT, 'tests/anchors'))) {
    out.anchors = fs.readdirSync(path.join(ROOT, 'tests/anchors')).sort();
  }
  return out;
}

function parseVerifyScripts() {
  const pkg = JSON.parse(read('package.json'));
  const verify = Object.entries(pkg.scripts).filter(([k]) =>
    k.startsWith('verify') || k.endsWith(':gates') || k === 'ci' || k.startsWith('production:'),
  );
  const qualityDir = path.join(ROOT, 'scripts/quality');
  const quality = fs.existsSync(qualityDir)
    ? fs.readdirSync(qualityDir).filter((f) => f.endsWith('.mjs')).sort()
    : [];
  return { verify, quality };
}

// --- Generate appendices ---

fs.mkdirSync(OUT, { recursive: true });

const { routes, pricingFlag } = parseRoutes();
const apiRoutes = parseApiRoutes();
const env = collectEnvVars();
const migrations = parseMigrations();
const scripts = parseScripts();
const deps = parseDeps();
const tests = globTests();
const verify = parseVerifyScripts();
const sha = gitSha();
const generatedAt = new Date().toISOString();

// A â€” routes
let a = `# Appendix A â€” Routes and API\n\nGenerated: ${generatedAt}\n\n## Client routes (\`src/routes.tsx\`)\n\n`;
a += `| Name | Path | Component | Access | Notes |\n|------|------|-----------|--------|-------|\n`;
for (const r of routes) {
  const notes = r.path === '/pricing' ? 'Flag: `VITE_PRICING_PAGE_ENABLED`' : '';
  a += `| ${r.name} | \`${r.path}\` | ${r.component} | ${r.access} | ${notes} |\n`;
}
a += `\nCatch-all \`*\` â†’ \`NotFound\` (see \`src/App.tsx\`).\n\n`;
a += `Pricing route conditional in source: \`PRICING_PAGE_ENABLED\` = ${pricingFlag}.\n\n`;
a += `## Serverless API (\`api/\`)\n\n| HTTP path | Handler file |\n|-----------|-------------|\n`;
for (const h of apiRoutes) {
  a += `| \`POST ${h.route}\` | \`${h.file}\` |\n`;
}
a += `\n## Shared API libraries\n\n`;
for (const f of fs.readdirSync(path.join(ROOT, 'api/_lib')).filter((x) => x.endsWith('.ts')).sort()) {
  a += `- \`api/_lib/${f}\`\n`;
}
write('A-routes-and-api.md', a);

// B â€” env vars
let b = `# Appendix B â€” Environment Variables\n\nGenerated: ${generatedAt}\n\n`;
b += `## Documented in \`.env.example\`\n\n`;
for (const k of env.exampleKeys) b += `- \`${k}\`\n`;
b += `\n## Client (\`import.meta.env\` / VITE_*)\n\n| Variable | Referenced in codebase |\n|----------|------------------------|\n`;
for (const k of env.vite) {
  b += `| \`${k.startsWith('VITE_') ? k : `VITE_${k}`}\` | yes |\n`;
}
for (const k of env.metaDev) {
  if (!env.vite.includes(k)) b += `| \`import.meta.env.${k}\` | yes (Vite built-in) |\n`;
}
b += `\n## Server (\`process.env\`)\n\n`;
for (const k of env.proc) b += `- \`${k}\`\n`;
const allCodeKeys = new Set([...env.vite.map((k) => (k.startsWith('VITE_') ? k : k)), ...env.proc]);
const undocumented = env.proc.filter((k) => !env.exampleKeys.includes(k) && !k.startsWith('PLAYWRIGHT') && !k.startsWith('GITHUB'));
b += `\n## Keys in code but not in \`.env.example\` (may be CI/operator-only)\n\n`;
for (const k of undocumented.sort()) b += `- \`${k}\`\n`;
write('B-environment-variables.md', b);

// C â€” npm scripts
let c = `# Appendix C â€” npm Scripts\n\nGenerated: ${generatedAt}\n\nTotal: ${scripts.length} scripts\n\n| Script | Command |\n|--------|--------|\n`;
for (const [name, cmd] of scripts) {
  c += `| \`${name}\` | \`${cmd.replace(/\|/g, '\\|')}\` |\n`;
}
write('C-npm-scripts.md', c);

// D â€” database
let d = `# Appendix D â€” Database Schema (from migrations)\n\nGenerated: ${generatedAt}\n\n`;
d += `## Migration files\n\n`;
for (const f of migrations.files) d += `- \`supabase/migrations/${f}\`\n`;
d += `\n## Tables (\`public\` schema)\n\n`;
for (const t of migrations.tables) d += `- \`${t}\`\n`;
d += `\n## Functions\n\n`;
for (const fn of migrations.functions) d += `- \`public.${fn.name}\` (${fn.file})\n`;
d += `\n## Policies\n\n| Policy | Table | Migration |\n|--------|-------|----------|\n`;
for (const p of migrations.policies) {
  d += `| ${p.name} | ${p.table} | ${p.file} |\n`;
}
d += `\n## Storage\n\n- Bucket \`materials\` (public read; user-scoped write) â€” \`20260213000005_collab_and_storage.sql\`\n`;
write('D-database-schema.md', d);

// E â€” verify
let e = `# Appendix E â€” Verification and Quality Gates\n\nGenerated: ${generatedAt}\n\n`;
e += `## npm verify / gates scripts\n\n| Script | Command |\n|--------|--------|\n`;
for (const [name, cmd] of verify.verify) {
  e += `| \`${name}\` | \`${cmd.replace(/\|/g, '\\|')}\` |\n`;
}
e += `\n## \`scripts/quality/*\`\n\n`;
for (const f of verify.quality) e += `- \`scripts/quality/${f}\`\n`;
e += `\n## Release gates\n\n13 gates enforced by \`scripts/verify-all.js\` â€” see [09-testing-quality-and-release.md](../09-testing-quality-and-release.md).\n`;
write('E-verify-scripts.md', e);

// F â€” tests
let f = `# Appendix F â€” Test Inventory\n\nGenerated: ${generatedAt}\n\n`;
f += `## Vitest unit/integration (${tests.unit.length} files)\n\n`;
for (const t of tests.unit) f += `- \`${t}\`\n`;
f += `\n## Playwright E2E (${tests.e2e.length} specs)\n\n`;
for (const t of tests.e2e) f += `- \`e2e/${t}\`\n`;
if (tests.anchors.length) {
  f += `\n## Regression anchors\n\n`;
  for (const t of tests.anchors) f += `- \`tests/anchors/${t}\`\n`;
}
write('F-test-inventory.md', f);

// G â€” deps
let g = `# Appendix G â€” Dependencies\n\nGenerated: ${generatedAt}\n\nPackage: \`${deps.name}\` v${deps.version}\n\n`;
g += `## Production (${deps.prod.length})\n\n| Package | Version |\n|---------|--------|\n`;
for (const [name, ver] of deps.prod) g += `| ${name} | ${ver} |\n`;
g += `\n## Development (${deps.dev.length})\n\n| Package | Version |\n|---------|--------|\n`;
for (const [name, ver] of deps.dev) g += `| ${name} | ${ver} |\n`;
g += `\nRun \`pnpm licenses list\` for license audit during due diligence.\n`;
write('G-dependencies.md', g);

// H â€” file tree
const treeRoots = ['src', 'api', 'server', 'supabase', 'scripts', 'e2e', 'docs', 'public', '.github'];
let h = `# Appendix H â€” Production File Tree (curated)\n\nGenerated: ${generatedAt}\n\n\`\`\`\n`;
for (const root of treeRoots) {
  if (!fs.existsSync(path.join(ROOT, root))) continue;
  h += `${root}/\n`;
  h += walkDir(path.join(ROOT, root), { maxDepth: 3 }).join('\n');
  h += '\n\n';
}
h += '```\n';
write('H-file-tree.md', h);

// MANIFEST.json
const manifest = {
  generatedAt,
  gitSha: sha,
  productVersion: deps.version,
  appendices: [
    'A-routes-and-api.md',
    'B-environment-variables.md',
    'C-npm-scripts.md',
    'D-database-schema.md',
    'E-verify-scripts.md',
    'F-test-inventory.md',
    'G-dependencies.md',
    'H-file-tree.md',
  ],
  counts: {
    routes: routes.length,
    apiHandlers: apiRoutes.length,
    npmScripts: scripts.length,
    tables: migrations.tables.length,
    unitTests: tests.unit.length,
    e2eSpecs: tests.e2e.length,
    prodDependencies: deps.prod.length,
    devDependencies: deps.dev.length,
  },
};
fs.writeFileSync(path.join(OUT, 'MANIFEST.json'), JSON.stringify(manifest, null, 2) + '\n');

console.log(`Handoff appendices generated at ${OUT}`);
console.log(`  git SHA: ${sha}`);
console.log(`  routes: ${routes.length}, api: ${apiRoutes.length}, scripts: ${scripts.length}`);
